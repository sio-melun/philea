<?php

namespace Cnes\PhileaBundle\Entity;

use Doctrine\ORM\Mapping as ORM;
use Symfony\Component\Validator\Constraints as Assert;

/**
 * Article
 *
 * @ORM\Table()
 * @ORM\Entity
 */
class Article
{   
    
    const SUPPRIMEE = -1;
    const ATTENTE_VALIDATION = 0;
    const VALIDE = 1;
    
